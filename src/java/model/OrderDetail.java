package model;

import java.util.Map;

/**
 *
 * @author trant
 */
public class OrderDetail {
    private int orderDetailId;
    private Order orderId;
    private Map<String, Object> productId;
    private int quantity;
    private double price;
    private String address;

    public OrderDetail() {
    }

    public OrderDetail(int orderDetailId, Order orderId, Map<String, Object> productId, int quantity, double price, String address) {
        this.orderDetailId = orderDetailId;
        this.orderId = orderId;
        this.productId = productId;
        this.quantity = quantity;
        this.price = price;
        this.address = address;
    }

    public int getOrderDetailId() {
        return orderDetailId;
    }

    public void setOrderDetailId(int orderDetailId) {
        this.orderDetailId = orderDetailId;
    }

    public Order getOrderId() {
        return orderId;
    }

    public void setOrderId(Order orderId) {
        this.orderId = orderId;
    }

    public Map<String, Object> getProductId() {
        return productId;
    }

    public void setProductId(Map<String, Object> productId) {
        this.productId = productId;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    
}
