    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        HttpSession session = request.getSession();
        User user = (User) session.getAttribute("User");

        if (user == null) {
            response.sendRedirect("login");
            return;
        }

        CartDAO cdao = new CartDAO();

        List<Order> listOrderByUser = cdao.getListCartByUserId(user.getUserId());

        request.setAttribute("orders", listOrderByUser);

        if (request.getParameter("id") != null) {
            String orderId = request.getParameter("id");
            List<OrderDetail> listdetail = cdao.getListCartDetailByOrderId(Integer.parseInt(orderId));
            request.setAttribute("listdetail", listdetail);
            request.getRequestDispatcher("orderDetailHistory.jsp").forward(request, response);
            return;
        }

        request.getRequestDispatcher("orderHistory.jsp").forward(request, response);
    }

